            <article class="blog-post">
              <h2 class="blog-post-title">Les tendances CV du moment</h2>
              <p class="blog-post-meta">January 1, 2021 by Par Nathalie Dépret</p>

              <h6>Le CV est le premier contact entre le candidat et le recruteur mais il se limite plus à un document qui relate la somme des expériences acquises. Pour Pauline Lahary, la fondatrice de my cv factory, le CV représente désormais ce qu’est le candidat dans sa globalité. Et c’est ce CV qui attire l’œil des entreprises.</h6>
              <hr>

              <h3>Quelles sont les tendances qui se dessinent autour des CV à l’heure actuelle ?</h3>
              <p>Depuis 5 ans, plusieurs tendances se sont succédées. La première vague a été le CV design marqué par l’arrivée des couleurs, de la fantaisie et des polices différentes. Si ce type de CV a tout à fait sa place dans certains secteurs comme le marketing, on voit aussi des CV en couleur dans la finance ou l’ingénierie alors qu’avant, le noir et blanc
                était de mise. Ensuite, il y a eu la digitalisation du CV car on craignait la disparition du CV papier. Cela permet au candidat d’inclure des liens comme son profil professionnel, son blog, ses réalisations en ligne, ses comptes Twitter ou Instagram selon le métier qu’on vise.
                A noter que ces deux types de CV cohabitent très bien ensemble et sont encore d’actualité. Aujourd’hui, la grande tendance qui touche tous les secteurs, c’est la valorisation des compétences et leurs mises en avant avec un contenu solide et chiffré. Ce CV est axé sur des mots clés qui vont appuyer ce que sait faire le collaborateur potentiel.</p>

              <h3>De quelles compétences s’agit-il et comment les articuler autour de l’expérience ?</h3>
              <p>Les expériences professionnelles restent importantes mais elles vont venir illustrer quatre types de compétences. Il y a tout d’abord les compétences métier comme la gestion de crise, la gestion de projet ou le management. Viennent ensuite les soft skills ou les compétences liées à la personnalité comme le leadership, la diplomatie ou encore l’empathie
                . La troisième catégorie de compétence repose sur la maitrise des outils - les hard skills - tels que les outils de programmation, de vente, de comptabilité et liée au métier en général. Pour finir, il y a les mad skills dont on parle de plus en plus aujourd’hui. Ce sont des compétences qui rendent le candidat extra ordinaire.
                Par exemple, un recruteur qui découvre qu’un candidat court des marathons va penser que celui-ci possède sans doute un mental d’acier. Tout le monde n’a pas forcément cette compétence et ce n’est pas grave mais on peut essayer de s’en rapprocher.</p>

              <h3>Ces mad skills sont-elles aisément reconnues par les recruteurs ?</h3>
              <p>Le terme ne leur sera pas forcément familier. A la place, on peut mettre ‘Mon petit plus’ ou ‘La cerise sur le gâteau’ pour les secteurs où on peut se permettre ce genre de petites phrases.</p>

              <h3>Ne faut-il pas énumérer des exemples liés à ses compétences ?</h3>
              <p>Absolument. Le candidat doit avoir en tête 3 exemples précis pour prouver chacune des compétences évoquées qu’il peut détailler par thème dans la case « Expériences Professionnelles ». Aujourd’hui, on voit beaucoup de CV avec 2 colonnes : à gauche, les compétences et à droite, l’expérience professionnelle.
                Le recruteur déchiffrant le CV de gauche à droite, il va le lire en diagonal en passant de l’une à l’autre. D’où l’intérêt de placer les compétences dans le bon ordre.</p>

              <h3>Désormais, on lie donc la compétence à l’expérience. Est-ce que cela signifie que le CV anti-chronologique passe à la trappe ?</h3>
              <p>Les CV anti-chronologiques existent encore mais l’idée est de mettre en avant compétences et expériences pour le métier que l’on vise. La difficulté avec ce type de CV, c’est de choisir les compétences qu’on veut valoriser et qui peuvent intéresser le recruteur. Pour cela, il faut s’inspirer de l’offre d’emploi et du site de l’entreprise pour trouver le mot clé, en adéquation avec le poste</p>

              <h3>D’où l’importance de cibler son CV en fonction de l’entreprise.</h3>
              <p>On ne demande pas à un candidat de changer son CV tous les jours mais les mots clés peuvent s’adapter au poste recherché. Si un candidat veut travailler dans le marketing, les mots clés seront sensiblement les mêmes.
                Mais s’il vise un poste dans la communication ou l’événementiel, il peut ajuster son CV dans ce sens grâce à certains mots clés dédiés.</p>

              <h3>Ce type de CV semble idéal pour le profil senior pour condenser une expérience importante.</h3>
              <p>En effet, un profil senior choisira plutôt le CV par compétence. En ce qui concerne les profils junior, même s’ils n’ont, a priori, pas la même expérience, il est néanmoins possible de faire la même chose. Le candidat peut se servir des activités qu’il a pu mener, cela aidera le
                recruteur à discerner le candidat qu’il est. Un profil très junior de 18 ans qui entre en école, peut mettre l’accent sur les compétences déployées via le sport, les voyages, les loisirs ou le bénévolat,
                des activités qui peuvent traduire une ouverture d’esprit ou un goût pour le travail d’équipe. Un junior diplômé de 25 ans, peut lui, se baser sur les stages effectués pendant lesquels il a acquis la maitrise de certains outils et donc des compétences à valoriser.</p>

              <h3>Faut-il mentionner les formations suivies sur ce CV par compétences ?</h3>
              <p>Bien sûr, à condition qu’elles ne datent pas de 1986. Mais un profil senior ne doit surtout pas hésiter à mettre en avant les formations suivies même celles mises en place au sein de l’entreprise. Cela lui permet de montrer que, tout au long de sa carrière, il a su rester au courant des évolutions liées à son métier.
                De son côté, un profil junior peut mettre en avant sa présence à des conférences dans son domaine ou des apéros networking. Cela montre qu’il est actif et ne compte pas se reposer sur ses lauriers</p>

