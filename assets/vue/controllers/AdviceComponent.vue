              <h2 class="blog-post-title">COMMENT TE PRÉPARER POUR TON ENTRETIEN ?</h2>
              <p class="blog-post-meta">December 14, 2020 by <a href="#">Chris</a></p>

              <h4>Avant l'entretien</h4>
              <h6>Se renseigner sur l'entreprise et le poste :</h6>
              <p>Renseigne-toi sur l’entreprise qui t'accueille, ses activités, le nombre de salariés, le nom et le parcours du ou des dirigeants, ses principaux clients. Il est rare que les entreprises n’aient pas de site internet, tu y trouveras une mine d’informations. Pour connaître l’actualité récente de l’entreprise rien de tel
                que de consulter les dernières coupures de presse qui la concerne, cela permet de montrer que tu t'intéresses à l’actualité, ce qui démontre une certaine curiosité d’esprit.</p>
              <ul>
                <li>Renseigne-toi aussi un maximum sur le poste :</li>
                <li>S’agit-il d’un travail en équipe ?</li>
                <li>Quels sont les horaires demandés ?</li>
                <li>Quel est le salaire ?</li>
                <li>Quelles sont les missions précises ?</li>
                <li>Le poste nécessite-t-il d’effectuer des déplacements ?</li>
                <li>Quelles sont les qualités attendues pour ce poste ?</li>
              </ul>
              <h6>S’entraîner :</h6>
              <p>Entraîne-toi ! Ce n’est pas facile de parler de soi en public et cela nécessite de l’entraînement. Tu apparaîtras encore plus naturel le jour J. Prépare quelques lignes sur ton parcours, tes qualités, tes envies et
                répète devant ton miroir ou devant des amis qui te diront en toute franchise ce qu’ils pensent de ta présentation. Tu peux également te filmer avec ton smartphone.</p>
              <p>Quelles sont les questions auxquelles il faut s’attendre :</p>
              <ul>
                <li>Pouvez-vous vous présenter ? Quel est votre parcours ?</li>
                <li>Connaissez-vous notre entreprise ? Que pouvez-vous nous en dire ?</li>
                <li>Pourquoi souhaitez-vous ce travail ?</li>
                <li>Qu’est-ce qui vous plaît dans notre secteur ?</li>
                <li>Etes-vous prêt à travailler en équipe ?</li>
                <li>Quelle est votre disponibilité ?</li>
                <li>Quelles sont vos prétentions salariales ?</li>
                <li>Quelles sont vos passions ?</li>
                <li>Quelles sont vos principales qualités ? défauts ?</li>
              </ul>
              <p>Prépare-toi également à poser des questions, si l’occasion se présente. Il n’est pas rare qu’un entretien se termine par
                « Avez-vous des questions ? ». Auquel cas, profites-en pour demander des précisions sur le poste ou la vie de l’entreprise, cela montrera que tu t'y projettes.</p>

              <h6>Préparer sa tenue avec soin :</h6>
              <p>Il faut se présenter sous son meilleur jour, être bien habillé. Sans en faire trop, n’y allez pas en smoking ! Reste sobre, avec des couleurs neutres et des vêtements bien repassés. Il faut également être à l’aise dans sa tenue, être propre sur soi et bien coiffé. L’apparence compte beaucoup, c’est la première chose que le recruteur voit, autant lui donner tout de suite une bonne impression.
                <br>
                Ne viens pas les mains vides et prépare les documents nécessaires : un petit cahier et un crayon, ainsi qu’une copie de ton CV et de ta lettre de motivation, de manière à être tout de suite prêt à prendre des notes si besoin et à laisser un document en partant. Tu peux aussi prendre l’annonce ou la convocation, ton attestation de stage ou de certificat de travail, ou encore une copie de tes diplômes.
                <br>
                Une fois ces étapes franchies, fonce à ton entretien et surtout : sois à l’heure ! N’hésite pas à calculer ton temps de transport afin d’arriver légèrement en avance. Un retard, même léger, laisserait une très mauvaise impression avant même d’avoir commencé.</p>

              <h4>Pendant l'entretien</h4>
              <p>Après la ponctualité et la tenue vestimentaire, la 3ème impression est la poignée de main. Elle doit être franche (sans être trop musclée), et tu dois regarder dans les yeux ton interlocuteur en le saluant. Attention ton corps parle pour toi ! Attention en période actuelle de Covid-19, il est conseillé de respecter les gestes barrières alors un salut de la tête sera préférable qu'une poignée de mains. </p>
              <h6>Contrôler ses gestes :</h6>
              <p>Si l’apparence vestimentaire est importante, la gestuelle pendant l’entretien l’est aussi. Une multitude de petits gestes peuvent traduire ton stress (jambe qui s’agite sous la table, doigts rongés, intonation tremblante de la voix, bras croisés…). Ne sois pas non plus trop détendu. Il ne faut pas s’avachir sur son siège, se balancer sur sa chaise, lever les yeux au ciel. Cela donnerait une impression de nonchalance peu souhaitable.
                <br>
                Ton regard est également important. Si plusieurs personnes sont en face de toi, veille à regarder chacune d’entre elles en leur parlant.
                <br>
                Tu as le droit sourire également, aussi cela doit rester le plus naturel possible (pas de sourire figé en permanence !).</p>
              <h6>Soigner son langage :</h6>
              <p>Parle de manière claire, posée, sans te précipiter. Si ton travail préparatoire a été correctement fait, tes réponses seront plus assurées. Et si tu n'as pas la réponse à une question, pas de panique ! Personne n’a réponse à tout. C’est aussi une force de savoir dire « je ne sais pas ».
                <br>
                Évite les tics de langage (« heu… », « hein ? », « alors », « voilà », "en faite") et bien entendu les fautes de français. C’est le jour ou jamais où tu dois soigner ton vocabulaire.
                <br>
                Veille à garder une attitude respectueuse et polie jusqu’au bout. Remercie bien tes interlocuteurs. On ne relâche la pression qu’une fois la porte refermée derrière soi !</p>

              <h4>Après l'entretien</h4>
              <p>N’hésite pas à adresser un mail de remerciement aux personnes qui t'ont reçues. C’est l’occasion de leur redire ta motivation pour obtenir le poste et l’intérêt que tu portes à l’entreprise.
                <br>
                Dis-leur que tu restes à leur disponibilité s’ils ont des questions complémentaires ou s’ils souhaitent te faire passer un deuxième entretien.
                <br>
                Pense à reporter les informations relatives à ton entretien (dates, coordonnées de la personne rencontrée, impressions) dans un tableau de suivi, qui te guidera tout au long de tes recherches de stage ou d’emploi.</p>
