              <h2 class="blog-post-title">Comment trouver un job qui m'épanouit ?</h2>
              <p class="blog-post-meta">December 23, 2020 by <a href="#">HéloïseGirard</a></p>

              <h6>On passe plus de la moitié de sa vie à travailler alors autant que ce soit pour un projet qui nous plaise ! On vous a dressé notre liste de conseil pour trouver le job qui vous permettra de nous épanouir... Alors plus d'excuse !</h6>
              <p>C’est notre but à tous. Nous passons plus de 75 000 heures à travailler dans notre vie, alors autant faire un job qui nous plaît vraiment. Mais pour ça, encore faut-il avoir défini ce qui va réellement jouer sur notre bonheur au travail. Et c’est loin d’être une tâche facile.</p>

              <h4>PAS DE FANTASME, LE JOB “PARFAIT” N’EXISTE PAS (MÊME EN STARTUP)</h4>
              <p>Avoir un job où l’on :</p>
              <ul>
                <li>Se lève tous les matins sans boule au ventre, tout en ayant des responsabilités</li>
                <li>A les mains dans le cambouis “mais pas trop”</li>
                <li>Décide de choses importantes sans en assumer les conséquences si cela échouait</li>
                <li>Sauve la planète, en faisant du business dans une entreprise en forte croissance</li>
                <li>Apprend beaucoup sans pour autant avoir de boss ou de mentor</li>
                <li>A un job hyper entrepreneurial tout en ayant des tickets resto, des RTT et un CE</li>
                <li>Apprend les méthodes agiles tout en étant dans un groupe de 20 000 personnes</li>
                <li>Évolue dans un cadre international, avec 0 différence culturelle avec ses collègues</li>
              </ul>
              <p>Cela tient... légèrement de l’ordre du fantasme. Car oui, l’emploi “parfait” n’existe pas. Et même les “jobs passion” ont des inconvénients : sans surprise, les plus grands concertistes ont des journées de répétition éreintantes, les grands sportifs passent leur
                vie à faire des sacrifices et les médecins souffrent de sous-effectifs à l’hôpital. Vous l’aurez compris, tout l’enjeu est de trouver le job qui vous rendra heureux. Et non de trouver “le job parfait”.</p>

              <h4>MAIS ALORS, COMMENT DÉFINIR LE JOB QUI VOUS ÉPANOUIRA ?</h4>
              <p>Quand on cherche le job qui nous rendra heureux, notamment en startup, on doit déjà définir une liste de critères qui auront un impact sur notre épanouissement professionnel. Voilà quelques exemples :</p>
              <ul>
                <li>L’équipe</li>
                <li>Les missions</li>
                <li>Les perspectives d’évolution</li>
                <li>Le produit ou le service</li>
                <li>L’impact sociétal ou environnemental</li>
                <li>La création de process</li>
                <li>L’apprentissage d’un métier</li>
                <li>Les valeurs de l’entreprise</li>
                <li>La politique de travail (télétravail, horaires définis, type de management etc.)</li>
                <li>etc.</li>
              </ul>

              <h4>COMMENT HIÉRARCHISER SES ATTENTES ?</h4>
              <p>Concrètement, si vous avez l’impression de vouloir “le beurre et l’argent du beurre” sur chacun de ces critères, c’est que vous faites fausse route. Pour bien faire,
                pensez à des vases communicants : si vous avez 80% d’un des critères, vous n’aurez plus que 20% de l’autre. Un exemple ? Vous cherchez un job entrepreneurial où vos actions ont un fort impact sur la vie de la startup. Après mûre réflexion, vous vous rendez compte que les 3 critères auxquels vous êtes le plus sensibles sont :
                apprendre un métier business et être dans une entreprise à forte croissance (60%), partager les valeurs de l’entreprise (40%). Et même si le secteur ou le contexte international restent pour vous des “nice to have” (20%), ces critères ne seront pas décisifs. En fait, vous avez 3 catégories :</p>
              <ul>
                <li>Ce qui sera indispensable, donc non négociable</li>
                <li>Ce qui sera négociable ou acceptable</li>
                <li>Ce qui ne viendra pas impacter votre décision</li>
              </ul>
              <p>Le plus simple, c’est alors de faire un tableau avec ces 3 catégories et de classer vos critères en fonction. Pour pousser la réflexion, hiérarchisez chacun de ces critères avec unenote de 1 à 10. Votre intuition et votre analyse de vos expériences passées vous aideront à faire cet exercice. Sans
                compter sur vos discussions avec les Talent Coach d’Elinoï : avec leur aide, vous pourrez passer “de l’autre côté de la porte” des startups pour confirmer ou infirmer qu’un job répond à vos critères pondérés.</p>

              <h4>ABORDEZ VOTRE RECHERCHE DE JOB AVEC LES IDÉES CLAIRES.</h4>
              <p>En ayant les idées claires, vous savez dans quelle direction vous partez.</p>
              <ul>
                <li>Cela vous aidera à ne viser que les jobs et entreprises qui répondent à un maximum de vos critères</li>
                <li>Cela vous rendra plus convaincant en entretien lorsqu’il s’agira d’expliquer par A + B en quoi ce job est fait pour vous</li>
                <li>Vous serez en paix avec votre choix final, et ne serez pas frustré(e) car vous aurez jaugé en amont ce qui est essentiel pour vous et ce que vous êtes prêts à abandonner.</li>
              </ul>

              <h4>PAS DE PANIQUE, VOS CRITÈRES NE RESTENT PAS GRAVÉS DANS LE MARBRE</h4>
              <p>Pas du tout, même. Vous structurez votre réflexion, certes mais cela ne vous empêche pas de faire deux ou trois scénarios différents. Et surtout, ne vous attendez pas à ce que vos critères pondérés restent inchangés dans le temps : ils vont évoluer, et c’est sain.Au même titre que vous serez souvent irrité(e) par les recherches de
                candidats “moutons à cinq pattes”, les recruteurs risquent aussi de l’être par des recherches de job “mouton à cinq (douze ?) pattes”. Un conseil ? Hiérarchisez ce qui est être important pour vous dans un job : cela donnera beaucoup plus de poids à ce que vous valorisez vraiment.</p>

